play_music:
    lw $t0, SET_INDEX  # grab set index

    # go to appropriate address of that set
    sll $t0, $t0, 2
    la $t1, MUSIC
    add $t0, $t1, $t0

    play_set:
        lw $t1, 0($t0)     # grab set address
        lw $t2, 0($t1)     # grab timestamp of set

        addi $t3, $t1, 4   # address of pitch
        addi $t4, $t1, 8   # address of duration
        addi $t5, $t1, 12  # address of instrument
        addi $t6, $t1, 16  # volume

    loop_set:
        lw $a0, 0($t3)  # pitch
        lw $a1, 0($t4)  # duration
        lw $a2, 0($t5)  # instrument
        lw $a3, 0($t6)  # volume

        # check if we have finished this set of notes
        beq $a0, -1, end_set
        beq $a1, -1, end_set
        beq $a2, -1, end_set
        beq $a3, -1, end_set
    
        # play note
        li $v0, 31
        syscall

        # move to next note
        addi $t3, $t3, 16
        addi $t4, $t4, 16
        addi $t5, $t5, 16
        addi $t6, $t6, 16

        j loop_set

