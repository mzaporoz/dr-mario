draw_paused:
    # Compute x_offset = (DISPLAY_WIDTH - image_width) / 2
    lw $t1, ADDR_DSPL
    lw    $t2, SCREEN_WIDTH      # t2 = display width
    lw    $t3, paused_width        # t3 = image width
    sub   $t2, $t2, $t3           # t2 = dispW - imgW
    sra   $t2, $t2, 1             # t2 = x_offset
    sll $t2, $t2, 2 # Multiply by 4 because x offset
    add  $t9, $t2, $t1             

    # Compute y_offset = (DISPLAY_HEIGHT - image_height) / 2
    lw    $t2, SCREEN_HEIGHT     # t2 = display height
    lw    $t3, paused_height       # t3 = image height
