check_rotation_collision:
    # $a0 contains how much to increment CURRENT_ADDRESS_1
    # $a1 contains how much to increment CURRENT_ADDRESS_2

    # Since we rotate the capsule by extending to the right, we need to check 
    # that this rotation does not collide with anything on the right.
    # If it does, we should push the capsule over to the left by 1 and continue to rotate.
    
    # Save $ra in the stack to return correctly
	save_ra()

    lw $t0, CURRENT_ADDRESS_1
    lw $t1, CURRENT_ADDRESS_2

    # Since the increments differ based on which part of the capsule
    # is on top, we have 2 separate functions
    beq $a0, 260, prep_check_next_rotate_1
    beq $a1, 260, prep_check_next_rotate_2
    beq $a0, -4, prep_check_next_rotate_1
    beq $a1, -4, prep_check_next_rotate_2

    save_registers()
    jal check_next_rotate
    restore_registers()

end_check_rotation_collision:
    restore_ra()
	jr $ra

prep_check_next_rotate_1:
    add $t2, $t0, $a0
    move $a2, $t2
    
    jal if_1_not_2_rotate
    
    j end_check_rotation_collision
    
prep_check_next_rotate_2:
    add $t3, $t1, $a1
    move $a2, $t3
    
    jal if_2_not_1_rotate

    j end_check_rotation_collision

if_1_not_2_rotate:
    bne $t2, $t1, check_next_rotate

    add $t3, $t1, $a1
    move $a2, $t3
    j check_next_rotate
    
    jr $ra

if_2_not_1_rotate:
    bne $t3, $t0, check_next_rotate

    add $t2, $t0, $a0
    move $a2, $t2
    j check_next_rotate
    
    jr $ra

check_next_rotate:
    # $a2 contains the address we are checking collision for
    
    lw $t7, BLACK
    lw $t8, 0($a2)

    bne $t7, $t8, change_rotation

end_check_next_rotate:
	j end_check_rotation_collision

change_rotation:
    # $a0 contains the increment on CURRENT_ADDRESS_1
    beq $a0, 260, change_rotate_1_on_top  # CURRENT_ADDRESS_1 is on top
    beq $a1, 260, change_rotate_2_on_top    # CURRENT_ADDRESS_2 is on top
    beq $a0, -4, cancel_rotate  # CURRENT_ADDRESS_1 is on right
    beq $a1, -4, cancel_rotate  # CURRENT_ADDRESS_2 is on right
    j end_check_next_rotate

change_rotate_1_on_top:
    li $a0, 256
    li $a1, -4
    # jr $ra
    j end_check_next_rotate

change_rotate_2_on_top:
    li $a0, -4
    li $a1, 256
    # jr $ra
    j end_check_next_rotate

cancel_rotate:
    jal collide_sound
    j end_check_rotation_collision

check_move_collision:
    # $a0 contains how much to increment CURRENT_ADDRESS_1
    # $a1 contains how much to increment CURRENT_ADDRESS_2
    
    # Save $ra in the stack to return correctly
	save_ra()
    
    lw $t0, CURRENT_ADDRESS_1
    lw $t1, CURRENT_ADDRESS_2

    # Grab the next CURRENT_ADDRESS_1
    add $t2, $t0, $a0

    # If next CURRENT_ADDRESS_1 != CURRENT_ADDRESS_2, check collision for next address 1
    move $a2, $t2                  # pass next address 1 as a parameter for check_not_black

    save_ra()
    jal if_1_not_2_move
    restore_ra()

    # Grab the next CURRENT_ADDRESS_2
    add $t3, $t1, $a1

    # If next CURRENT_ADDRESS_2 != CURRENT_ADDRESS_1, check collision for next address 2
    move $a2, $t3                  # pass next address 2 as paramater for check_not_black 

    save_ra()
    jal if_2_not_1_move
    restore_ra()

	jr $ra

if_1_not_2_move:
    bne $t2, $t1, check_not_black
    jr $ra

if_2_not_1_move:
    bne $t3, $t0, check_not_black
    jr $ra

check_not_black:    
    lw $t7, BLACK
    lw $t8, 0($a2)

    bne $t7, $t8, cancel_move
	jr $ra
    
cancel_move:
    # if the collision is from below, the capsule has landed
    beq $a0, 256, capsule_landed
    beq $a1, 256, capsule_landed

    # if not, play collide sound
    jal collide_sound

    restore_ra()
	j game_loop

capsule_landed:
    # Before checking for matches, store capsules' ids
    # sll $a0, $a0, 24
    # $t0 stores one part of the capsule, $t1 stores the other

    lw $t2, ID_COUNTER
    # Set up a mask to clear the upper 8 bits (lower 24 bits are preserved)
    li   $t3, 0x00FFFFFF      # $t3 = mask 0x00FFFFFF

    # Shift the capsule ID into the upper 8 bits
    sll  $t4, $t2, 24         # $t4 = capsule_id << 24

    # Process the first capsule pixel at address in $t0
    lw   $t5, 0($t0)          # Load pixel from first capsule cell into $t5
    and  $t5, $t5, $t3        # Clear upper 8 bits: $t5 = $t5 & 0x00FFFFFF
    or   $t5, $t5, $t4        # Insert capsule id: $t5 = ($t5 & mask) | (capsule_id<<24)
    sw   $t5, 0($t0)          # Store updated pixel back to first capsule cell

    # Process the second capsule pixel at address in $t1
    lw   $t5, 0($t1)          # Load pixel from second capsule cell
    and  $t5, $t5, $t3        # Clear upper 8 bits
    or   $t5, $t5, $t4        # Insert capsule id
    sw   $t5, 0($t1)          # Store updated pixel back to second capsule cell

    addi $t2, $t2, 1

    sw $t2, ID_COUNTER

    save_ra()
    # finalize the capsule on the board
    jal finalize_capsule
    restore_ra()

    save_registers()
    # spawn a new capsule
    jal draw_capsule
    restore_registers()

    jal land_sound
    
	j game_loop

finalize_capsule:
    save_ra()

    # check for 4 in a row
    jal check_matches
    
    # drop floating pieces
    
    restore_ra()
    jr $ra

check_matches:
    save_ra()

    # 1. Check horizontal 4+ in a row
    # Grab the first address of the bottle space (top left)
    jal get_leftmost_coordinate 
    lw $a0, 0($sp)     
    addi $sp, $sp, 4   # $t0 now contains the (top) left most coordinate of the bottle
    addi $a0, $a0, 260 # since $t0 is coord of bottle edge, need to jump row and col to get the top left coord of bottle space

    # Grab number of rows to iterate through
    lw $t1, HEIGHT_BOTTLE
    subi $t1, $t1, 2  # subtract 2 to account for bottle edges

    li $t2, 0    # row counter
    li $a1, 4    # increment amount for 4 in a row counter (4 for horizontal)
    li $a2, 256  # increment amount for row counter

    jal matches_loop

    # 2. Check vertical 4+ in a row
    # Grab the first address of the bottle space (top left)
    jal get_leftmost_coordinate 
    lw $a0, 0($sp)     
    addi $sp, $sp, 4   # $t0 now contains the (top) left most coordinate of the bottle
    addi $a0, $a0, 260 # since $t0 is coord of bottle edge, need to jump row and col to get the top left coord of bottle space
    
    # Grab number of cols to iterate through
    lw $t1, WIDTH_BOTTLE
    subi $t1, $t1, 2  # subtract 2 to account for bottle edges

    li $t2, 0    # col counter
    li $a1, 256  # increment amount for 4 in a row check (256 for vertical)
    li $a2, 4    # increment amount for col counter
    jal matches_loop

    # li $a2, 256
    # jal row_loop

    restore_ra()
    jr $ra

matches_loop:
    # $a0 contains the starting address
    # $a1 contains the increment amount
    # $t1 contains the number of rows to iterate through
    # $t2 contains the row counter (starting at 0)

    save_ra()
    
    bge $t2, $t1, end_matches_loop  # exit if row counter >= rows

    move $t0, $a0  # move starting address into t0

    jal check_row

    add $a0, $a0, $a2
    addi $t2, $t2, 1

    restore_ra()
    j matches_loop

end_matches_loop:
    jr $ra

check_row:
    # $a0 contains starting address of the row
    # $t0 contains starting address of the row (to change)
    li $t3, 1  # counter for consecutive colours

    lw $t4, MASK
    
    lw $t5, GRAY       # load border colour
    and $t5, $t5, $t4  # mask
    
    lw $t9, BLACK  # load background colour

    lw $t8, COLOR_MASK

row_loop:
    # $t0 contains starting address of the row (to change)
    # $t3 contains the counter for consecutive colours
    # $t4 contains the mask
    # $t5 contains the colour of the border edge

    lw $t6, 0($t0)     # load current colour
    and $t6, $t6, $t8
    
    and $t6, $t6, $t4  # mask

    beq $t6, $t9, skip_black    # if colour is black, do not count
    beq $t6, $t5, end_row_loop  # if we reach the border edge, exit

    add $t7, $t0, $a1
    
    lw $t7, 0($t7)     # load next colour
    and $t7, $t7, $t8
    
    and $t7, $t7, $t4  # mask

    beq $t6, $t7, increment_count  # if match, increase count
    bge $t3, 4, found         # if 4+ matches, clear
    li $t3, 1                      # if no match, reset count
    # move $t8, $t0                  # store start of new sequence

    j next

skip_black:
    add $t0, $t0, $a1
    j row_loop

increment_count:
    addi $t3, $t3, 1
    j next

next:
    add $t0, $t0, $a1
    j row_loop
    
end_row_loop:
    bge $t3, 4, found  # if 4+ matches, break
    jr $ra

found:
    # $t0 contains the current address of the sequence end
    # $t3 contains the number of consecutive colours
    # $t6 contains the colour of the current sequence

    mul $t3, $t3, $a1    # multiply count by increment amount
    sub $t0, $t0, $t3    # subtract from end of sequence
    add $t0, $t0, $a1    # move forward by one to get start of sequence
    lw $t8, COLOR_MASK
    
clear_loop:
    # $t0 contains the start address of the sequence
    # $t6 contains the colour of the sequence
    # $t9 contains the colour we are clearing to (black)

    lw $t7, 0($t0)

    save_registers()

    jal check_if_virus

    restore_registers()
    
    
    and $t7, $t7, $t8
    and $t7, $t7, $t4
    bne $t7, $t6, end_clear_loop  # if the colour changes, exit

    sw $t9, 0($t0)  # clear colour of current address

    move $a3, $t0  # store start of sequence into $a3 to use for drop_column

    add $t0, $t0, $a1
    
    j clear_loop

check_if_virus:
  # $t7 contains the color of the pixel

  lw $t0, VIRUS_BLUE

  lw $t1, VIRUS_RED

  lw $t2, VIRUS_YELLOW

  beq $t7, $t0, decrement_num_viruses

  beq $t7, $t1, decrement_num_viruses

  beq $t7, $t2, decrement_num_viruses

  jr $ra

  
decrement_num_viruses:
  lw $t9, CUR_NUM_VIRUSES
  subi $t9, $t9, 1
  sw $t9, CUR_NUM_VIRUSES

  jr $ra
  

end_clear_loop:

    addi $s2, $s2, 1  # increase clear count

    jal clear_sound
  
    save_registers()
    
    jal drop_all_columns

    restore_registers()
    
    li $t3, 1  # reset count

    # at this point, we are one pixel ahead of the colour sequence end.
    # since we are calling j next which sends us one pixel forward, 
    # we should go back to the colour sequence end so that we don't skip a pixel.
    sub $t0, $t0, $a1

    save_ra()

    jal check_matches     # continue searching for more 4+ in a row

    restore_ra()
    restore_ra()

    jr $ra

drop_all_columns:
  # First, we need to get the leftmost down coordinate of the bottle

  save_ra()

  jal get_leftmost_coordinate

  # $t0 stores the leftmost coordinate of the bottle
  lw $t0, 0($sp) #$t2 stores the leftmost coordinate of the map  
  addi $sp, $sp, 4 

  lw $t3, HEIGHT_BOTTLE
  subi $t3, $t3, 1

  sll $t3, $t3, 8

  add $t0, $t0, $t3

  subi $t0, $t0, 252

  # now $t0 stores the leftmost coordinate of the bottle

  # At this point, we start iterating over each pixel in each column and checking if it should be dropped

  move $a0, $t0
  lw $a1, COLOR_MASK


drop_loop_all_columns:
  # $a0 stores the downmost coordinate of the column -> after iterations, stores the current capsule

  lw $t9, 0($a0) # Load the color of the pixel

  and $t9, $t9, $a1

  lw $t8, GRAY

  beq $t9, $t8, end_drop_all

  save_registers()

  jal drop_all_in_vertical_loop

  restore_registers()

  addi $a0, $a0, 4

  j drop_loop_all_columns

end_drop_all:
  restore_ra()
  jr $ra

drop_all_in_vertical_loop:
  # $a0 stores the current coordinate of the pixel in the vertical line

  # store the color of the pixel in $t0
  lw $t0, 0($a0)

  and $t0, $t0, $a1

  lw $t9, RED
  lw $t8, BLUE

  lw $t7, YELLOW

  lw $t6, GRAY
  
  beq $t0, $t9, check_if_can_drop
  beq $t0, $t8, check_if_can_drop
  beq $t0, $t7, check_if_can_drop

  beq $t0, $t6, end_drop_vertical_loop

  subi $a0, $a0, 256

  j drop_all_in_vertical_loop

end_drop_vertical_loop:
  jr $ra
  

check_if_can_drop:
  # $a0 contains the pixel that is the potential candidate to drop
  # If there is a black pixel below it and it is not supported by its other half, we drop

  addi $t1, $a0, 256 # Get the address of the pixel below

  lw $t9, 0($t1) # Load the color of the pixel

  and $t9, $t9, $a1

  beq $t9, 0, check_left

  subi $a0, $a0, 256

  j drop_all_in_vertical_loop


check_left:
  # Check if there is another part of the capsule on the left -> if at least one of them has no pixel below, drop
  # $a0 stores the current address
  subi $t3, $a0, 4 # Pixel on the left

  lw $t9, 0($t3) 

  srl $t9, $t9, 24 # Now $t9 contains the ID of the potential half pixel 

  lw $t8, 0($a0)

  srl $t8, $t8, 24 # Now $t8 contains the ID of the initial pixel

  beq $t8, $t9, check_left_below # The IDs are equal -> check if supported

  j check_right


check_right:
  # $a0 stores current pixel
  addi $t3, $a0, 4
  lw $t9, 0($t3)

  srl $t9, $t9, 24 # Now $t9 contains the ID of the potential half pixel 

  lw $t8, 0($a0)

  srl $t8, $t8, 24 # Now $t8 contains the ID of the initial pixel

  beq $t8, $t9, check_right_below

  # If we are here, then we know that the pixel below is black, pixel on the left is not the other half, the pixel on the right is not the other half => can drop the pixel

  add $a1, $zero, $zero
  
  j drop_capsule
  

check_right_below:
  # $a0 stores current capsule
  # $t3 stores capsule on the right
  addi $t4, $t3, 256 # Store the capsule below

  lw $t9, 0($t4)
  beq $t9, 0, confirm_drop_column

  # Here we know that there was no part of the capsule on the left but was one on the right -> yet, it is supported -> don't drop
  subi $a0, $a0, 256

  j drop_all_in_vertical_loop
  

check_left_below:
  # $t3 stores pixel on the left
  # $a0 stores current pixel
  addi $t4, $t3, 256 # Store the capsule below

  lw $t9, 0($t4)
  beq $t9, 0, confirm_drop_column

  subi $a0, $a0, 256

  j drop_all_in_vertical_loop
  

confirm_drop_column:
    # Call drop_column with starting pixel stored in below $a3

    move $a1, $t3
    
    save_registers()

    jal drop_capsule

    restore_registers()
    
    j check_matches

drop_capsule:
  # $a0 -> one half
  # $a1 -> another half

  lw $t4, BLACK
  
  beq $a1, 0, drop_solo


drop_capsule_loop:
  # $a0 -> one half
  # $a1 -> another half

  lw $t1, 0($a0) # Stores color + id

  lw $t2, 0($a1) # Stores color + id

  addi $t8, $a0, 256 # Pixel below one half

  lw $t5, 0($t8) # Color of that half
  
  addi $t9, $a1, 256 # Pixel below another half

  lw $t6, 0($t9) # Color of that half

  bne $t5, 0, end_drop_capsule_loop

  bne $t6, 0, end_drop_capsule_loop
  
  # Here we know that the pixels below both parts of the capsule are 0
  
  sw $t1, 0($t8)

  sw $t2, 0($t9)

  sw $t4, 0($a0)

  sw $t4, 0($a1)

  addi $a0, $a0, 256

  addi $a1, $a1, 256

  save_registers()
  
  jal transfer_to_bitmap

  restore_registers()

  j drop_capsule_loop

drop_solo:
  # $a0 contains the address of the single half

  lw $t1, 0($a0) # Stores color + id

  addi $t8, $a0, 256 # Pixel below one half

  lw $t5, 0($t8) # Color of that half

  bne $t5, 0, end_drop_capsule_loop

  sw $t1, 0($t8)

  sw $t4, 0($a0)

  addi $a0, $a0, 256

  save_registers()

  jal transfer_to_bitmap

  restore_registers()

  j drop_solo
  

end_drop_capsule_loop:
  j drop_all_columns

    
check_if_entrance_blocked:
  # Calculate the position of the pixel that is the first pixel on the map below dropping capsule
  addi $sp, $sp, -4
  sw $ra, 0($sp)
  
  jal get_leftmost_coordinate

  lw $t2, 0($sp) #$t2 stores the leftmost coordinate of the map  
  addi $sp, $sp, 4 

  addi $t2, $t2, 256
  # Load width of the bottle
  lw $t4, WIDTH_BOTTLE
  subi $t4, $t4, 1
  sra $t4, $t4, 1 # Divide by 2 to get the middle point

  sll $t4, $t4, 2 # Multiply by 4 to convert into bytes

  add $t2, $t2, $t4 # Now $t4 contains the mid of the bottle neck

  lw $t5, CURRENT_ADDRESS_1
  lw $t6, CURRENT_ADDRESS_2

  beq $t2, $t5, end_continue
  beq $t2, $t6, end_continue
  lw $t3, 0($t2) # Load the color

  beq $t3, 0, end_continue
  j end_blocked

end_continue:
  lw $ra, 0($sp)
  addi $sp, $sp, 4
  jr $ra

end_blocked:
  jal game_over_sound
  j game_over



game_over:
  # Step 0: reset SET_INDEX

  li $t1, 0

  sw $t1, SET_INDEX
  
  # First, delete the whole data section and transfer to bitmap to make the screen black

  la $t1, BOARD # Starting coordinate

  lw $t3, BLACK

  li, $t2, 0 # Counter of pixels visited

  save_registers()

  jal make_screen_black_loop

  restore_registers()

  # Now, draw start 

  save_registers()

  jal draw_start

  restore_registers()

  # Now make start toggle and wait for "enter" keyboard to restart the game

  j start_toggle_and_wait


start_toggle_and_wait:
  # 1a. Check if key has been pressed
  lw $t0, ADDR_KBRD               # $t0 = base address for keyboard
  lw $t1, 0($t0)                  # Load first word from keyboard


  save_registers()
  # 1b. Check which key has been pressed
  beq $t1, 1, check_if_q_or_enter      # If first word 1, key is pressed

  restore_registers()

  # Here, we draw start into BLACK
  lw $t1, ADDR_DSPL
  lw    $t2, SCREEN_WIDTH      # t2 = display width
  lw    $t3, toggle_width        # t3 = image width
  sub   $t2, $t2, $t3           # t2 = dispW - imgW
  sra   $t2, $t2, 1             # t2 = x_offset
  sll $t2, $t2, 2 # Multiply by 4 because x offset
  add  $t9, $t2, $t1             

  # Compute y_offset = 4
  li $t2, 32
  sll $t2, $t2, 8 # Multiply by 256 because y offset

  add $a0, $t9, $t2

  lw $a1, toggle_height
  lw $a2, toggle_width

  la $a3, toggle_pixels

  # t0 will be our row index (i). Initialize to 0.
  li    $t3, 0 

  save_registers()
  
  jal row_paused_loop

  restore_registers()

  # Here, we sleep

  li $v0, 32
  li $a0, 512
  syscall

  # Redraw Start

  save_registers()

  jal draw_start

  restore_registers()

  # Here, we sleep

  li $v0, 32
  li $a0, 512
  syscall

  # Loop back

  j start_toggle_and_wait


check_if_q_or_enter:
  lw $a0, 4($t0)                  # Load second word from keyboard

  beq $a0, 0x71, exit     # Check if the key q was pressed

  beq $a0, 0x0A, menu

  j start_toggle_and_wait


draw_start:
  
  # Compute x_offset = (DISPLAY_WIDTH - image_width) / 2
    lw $t1, ADDR_DSPL
    lw    $t2, SCREEN_WIDTH      # t2 = display width
    lw    $t3, start_width        # t3 = image width
    sub   $t2, $t2, $t3           # t2 = dispW - imgW
    sra   $t2, $t2, 1             # t2 = x_offset
    sll $t2, $t2, 2 # Multiply by 4 because x offset
    add  $t9, $t2, $t1             

    # Compute y_offset = 4
    li $t2, 32
    sll $t2, $t2, 8 # Multiply by 256 because y offset

    add $a0, $t9, $t2

    lw $a1, start_height
    lw $a2, start_width

    la $a3, start_pixels

    # t0 will be our row index (i). Initialize to 0.
    li    $t3, 0 

    save_registers()
    
    jal row_paused_loop

    restore_registers()

    jr $ra
  


make_screen_black_loop:
  beq $t2, 10240, draw_game_over

  sw $t3, 0($t1)

  addi $t1, $t1, 4

  addi $t2, $t2, 4

  j make_screen_black_loop

draw_game_over:
  save_registers()

  jal transfer_to_bitmap

  restore_registers()
  
  # Compute x_offset = (DISPLAY_WIDTH - image_width) / 2
    lw $t1, ADDR_DSPL
    lw    $t2, SCREEN_WIDTH      # t2 = display width
    lw    $t3, game_over_width        # t3 = image width
    sub   $t2, $t2, $t3           # t2 = dispW - imgW
    sra   $t2, $t2, 1             # t2 = x_offset
    sll $t2, $t2, 2 # Multiply by 4 because x offset
    add  $t9, $t2, $t1             

    # Compute y_offset = 4
    li $t2, 0
    sll $t2, $t2, 8 # Multiply by 256 because y offset

    add $a0, $t9, $t2

    lw $a1, game_over_height
    lw $a2, game_over_width

    la $a3, game_over_pixels

    # t0 will be our row index (i). Initialize to 0.
    li    $t3, 0 

    save_registers()
    
    jal row_paused_loop

    restore_registers()

    jr $ra


draw_map:
    addi $sp, $sp, -4
    sw $ra, 0($sp)

    jal get_leftmost_coordinate

    lw $t0, 0($sp) #$t0 stores the leftmost coordinate of the map  
    addi $sp, $sp, 4    
    
    # Load the width of the bottle
    lw $t3, WIDTH_BOTTLE
    lw $t4, HEIGHT_BOTTLE
    lw $t5, WIDTH_NECK
    lw $t6, HEIGHT_NECK
    lw $t7, GRAY
    # Pass the leftmost coordinate, width of the bottle, height of the bottle, width of the bottle neck, height of the bottle neck, colour of the bottle to the function
    # Save $ra register to return correctly

    # Set colour
    move $a0, $t7
    # Set width of the bottle
    move $a1, $t3
    # Set height of the bottle
    move $a2, $t4
    # Set width of the neck
    move $a3, $t5
    # Push height of the neck
    addi $sp, $sp, -4
    sw $t6, 0($sp)
    # Push the location of the beginning of the scene
    addi $sp, $sp, -4
    sw $t0, 0($sp)

    jal draw_scene

    # WE'RE DONE DRAWING THE SCENE -> DON'T CARE ABOUT ANY OF THE REGISTERS

    # Call the function that draws the first capsule at the needed location

    jal draw_capsule

    jal draw_viruses


    save_registers()

    jal transfer_to_bitmap

    restore_registers()

    save_registers()

    jal draw_background

    restore_registers()
    
    # Transfer the resulting game map to the bitmap
    
    jal transfer_to_bitmap

    lw $ra, 0($sp)   # Restore return address
    addi $sp, $sp, 4    
    jr $ra

transfer_to_bitmap:
    # $t0 stores the counter
    # $t1 stores the address of the game map
    # $t3 stores the current pixel on the bitmap
    la $t1, BOARD
    add $t0, $zero, $zero
    lw $t3, ADDR_DSPL
 
    addi $sp, $sp, -4
    sw $ra, 0($sp)

    lw $t8, COLOR_MASK

    jal transfer_loop

    lw $ra, 0($sp)   # Restore return address
    addi $sp, $sp, 4    
    jr $ra

transfer_loop:
    beq $t0, 4096, end_transfer_loop
    lw $t4, 0($t1)
    # Get the color from the pixel
    and $t4, $t4, $t8 # Mask out the upper 8 bits; $t4 now contains 0xRRGGBB.
    
    sw $t4, 0($t3) # Draw the colour into the current pixel
    addi $t0, $t0, 1 # Increment incrementer
    lw $t9, ADDR_DSPL
    add $t1, $t1, 4 # Increment current pixel
    addi $t3, $t3, 4 # Increment pixel on the bitmap
    j transfer_loop

end_transfer_loop:
    jr $ra
  
draw_scene:
    # $a0 stores the colour
    # $a1 stores the width of the bottle
    # $a2 stores the height of the bottle
    # $a3 stores the width of the bottle neck
    # $t0 stores the height of the bottle neck
    # $t1 stores the location of the current pixel of the scene that we point toward
    # $t4 stores an increment required for each type of line -> 256 for down, 4 for right, -4 for left, -256 for up
    
    # Read location of the beginning of the scene from the stack
    lw $t1, 0($sp)
    addi $sp, $sp, 4
    # Read height of the bottle neck from the stack
    lw $t0, 0($sp)
    addi $sp, $sp, 4

    # Push $ra register to the stack to save the return register
    addi $sp, $sp, -4
    sw $ra, 0($sp)
    
    # First, draw the rectangle base
    jal draw_rectangle

    jal draw_neck

    # here, draw background

    lw   $ra, 0($sp)    # Restore $ra
    addi $sp, $sp, 4    # Restore stack pointer
    jr   $ra            # Return to caller

  
draw_rectangle:
    # $t1 stores the location of the current pixel -> get this from the function above as we will stop at the last pixel and pass it on to new calls
    # $a1 stores the width of the bottle
    # $a2 stores the height of the bottle
    # $a0 stores the colour

    # Save $ra in the stack to return correctly
    addi $sp, $sp, -4
    sw $ra, 0($sp)

    # Draw the left wall 
  
    # Initialize increment $t3
    # Store the pixel increment for drawing down in $t4 -> this time it's 256
    add $t3, $zero, $zero #$t3 is the increment that stores how many pixels we drew - essentialy "int i"
    add $t4, $zero, 256
    # Store the height of the bottle to $t2 to use in the draw_loop
    move $t2, $a2
    jal draw_loop

    # Adjust the pixels so that we don't go extra
    addi $t1, $t1, -252
    subi $a1, $a1, 2
  
    # Now draw the bottom part of the bottle
    add $t3, $zero, $zero #$t3 is the increment 
    add $t4, $zero, 4
    # Store the width of the bottle to $t2 to use in the draw_loop
    move $t2, $a1
    jal draw_loop

    # Now draw the right wall
    add $t3, $zero, $zero #$t3 is the increment 
    add $t4, $zero, -256
    # Store the width of the bottle to $t2 to use in the draw_loop
    move $t2, $a2
    jal draw_loop

    # Adjust pixels
    add $t1, $t1, 252

    lw $ra, 0($sp)   # Restore return address
    addi $sp, $sp, 4
    jr $ra

draw_neck:
    # $t1 stores the location of the current pixel -> get this from the function above as we will stop at the last pixel and pass it on to new calls
    # $a1 stores the width of the bottle
    # $a3 stores the width of the bottle neck
    # $t0 stores the height of the bottle neck
    # $a0 stores the colour

    # Save $ra in the stack to return correctly
    addi $sp, $sp, -4
    sw $ra, 0($sp)

    # Compute the width of each part in $59
    sub $t9, $a1, $a3 # Substract parts
    sra $t9, $t9, 1 # Divide by 2

    # Draw right line

    add $t3, $zero, $zero #$t3 is the increment 
    add $t4, $zero, -4
    # Store the width of the neck to $t2 to use in the draw_loop
    move $t2, $t9
    jal draw_loop

    # Adjust the pixel
    addi $t1, $t1, -252

    # Reduce the height by 1 as we already have 1 pixel drawn -> store it in $t8
    subi $t8, $t0, 1

    add $t3, $zero, $zero #$t3 is the increment 
    add $t4, $zero, -256
    # Store the width of the neck to $t2 to use in the draw_loop
    move $t2, $t8
    jal draw_loop

    # Adjust the pixel to the opposite side
    addi $t1, $t1, 252 # Get it back to the previous pixel
    sll $t7, $a3, 2
    neg $t7, $t7
    add $t1, $t1, $t7

    # Start drawing the line down
    add $t3, $zero, $zero #$t3 is the increment 
    add $t4, $zero, 256
    # Store the height of the neck to $t2 to use in the draw_loop
    move $t2, $t8
    jal draw_loop

    # Draw the last left horizontal line
    add $t3, $zero, $zero #$t3 is the increment 
    add $t4, $zero, -4
    # Store the width of the neck to $t2 to use in the draw_loop
    move $t2, $t9
    jal draw_loop
  
    lw $ra, 0($sp)   # Restore return address
    addi $sp, $sp, 4
    jr $ra
  
draw_loop:
    # This loop draws line up $a2 (length) pixels
    beq $t3, $t2, end_draw_loop # While index == number of pixels, loop
    sw $a0, 0($t1) # Draw the colour into the current pixel
    addi $t3, $t3, 1 # Increment incrementer
    add $t1, $t1, $t4 # Increment current pixel
    j draw_loop
  
end_draw_loop:
    jr $ra

draw_capsule:

    save_registers()

    jal transfer_to_bitmap

    restore_registers()
  
    # $t1 stores a colour of one part
    # $t0 stores a colour of another part
    # 0 - red
    # 1 - blue
    # 2 - yellow

    # Push $ra register to the stack to save the return register
    save_ra()

    # Calculate where the pixel should be located -> left most coordinate + mid of the width bottle
    # Store the result in $t2

    jal get_leftmost_coordinate

    lw $t2, 0($sp) #$t0 stores the leftmost coordinate of the map  
    addi $sp, $sp, 4 

    # Load width of the bottle
    lw $t4, WIDTH_BOTTLE
    subi $t4, $t4, 1
    sra $t4, $t4, 1 # Divide by 2 to get the middle point

    sll $t4, $t4, 2 # Multiply by 4 to convert into bytes

    add $t2, $t2, $t4 # Now $t4 contains the mid of the bottle neck

    # Now we need to put it up on HEIGHT_NECK - 1

    lw $t4, HEIGHT_NECK
    subi $t4, $t4, 2 # Reduce the heigh by 2 as we are on the first pixel already
    sll $t4, $t4, 8 # Multiply by 256 because we are moving vertically

    sub $t2, $t2, $t4

    # store the addresses of the capsule into CURRENT_ADDRESS_1

    sw $t2, CURRENT_ADDRESS_1

    # Generate random number from 0 to 2
    li $v0, 42
    li $a0, 0
    li $a1, 3
    syscall

    # Store the result in $t0
    move $t0, $a0
  
    # Move the colour picker into $t0
    move $a0, $t0
    jal get_colour

    # The colour is stored in $t5
    lw $t5, 0($sp) # $t5 stores the color
    addi $sp, $sp, 4

    sw $t5, 0($t2)

    sw $t5, CURRENT_COLOR_1

    # Move pixel down
    addi $t2, $t2, 256

    sw $t2, CURRENT_ADDRESS_2

    # Generate random number from 0 to 2
    li $v0, 42
    li $a0, 0
    li $a1, 3
    syscall

    # Store the result in $t1
    move $t1, $a0
  
    # Move the colour picker into $t0
    move $a0, $t1

    jal get_colour # Get colour of the second pixel
    lw $t5, 0($sp) # $t5 stores the color
    addi $sp, $sp, 4
  
    sw $t5, 0($t2) # Store it

    sw $t5, CURRENT_COLOR_2

    save_registers()

    jal transfer_to_bitmap

    restore_registers()
  
    restore_ra()
    jr $ra

get_colour:
    # Case 0 -> Red
    lw   $t5, RED       # Prepare red in $t5
    beq  $a0, $zero, end_colour  # if ($a0 == 0) => done
  
    # Case 1 -> Blue
    lw   $t5, BLUE       # Prepare blue in $t5
    li   $t9, 1
    beq  $a0, $t9, end_colour   # if ($a0 == 1) => done
  
    # Case 2 -> Yellow
    lw   $t5, YELLOW

end_colour:
    addi $sp, $sp, -4
    sw $t5, 0($sp)
    jr $ra                  # Return (output stored in $t5)

draw_viruses:
    # x can be from 1 to 24
    # y can be from 7 to 31
    # Randomly generate x and y coordinates 3 times and draw them on the game map

    # Before storing value, gotta check if it's empty -> might be a chance that they were mapped to the same spot

    # $t0 stores the number of viruses in our game
    lw $a3, NUM_VIRUSES

    # $a2 will count how many we have already added
    add $a2, $zero, $zero

    addi $sp, $sp, -4
    sw $ra, 0($sp)

viruses_loop:
    beq $a2, $a3, end_viruses_loop

    # randomly get x coordinate
    li $v0, 42
    li $a0, 0
    li $a1, 20
    syscall

    # store x coordinate in $t3
    move $t3, $a0
    addi $t3, $t3, 2

    li $v0, 42
    li $a0, 0
    li $a1, 20
    syscall

    # store y coordinate in $t4
    move $t4, $a0

    addi $t4, $t4, 7

    # generate random color
    li $v0, 42
    li $a0, 0
    li $a1, 3
    syscall

    # store random color in $t8 

    move $t8, $a0

    # find the coordinates of the virus
    # First, load the leftmost get_leftmost_coordinate to $t9

    jal get_leftmost_coordinate

    lw $t9, 0($sp) #$t0 stores the leftmost coordinate of the map  
    addi $sp, $sp, 4  
  
    sll $t4, $t4, 8 # multiply y coordinate by 128
    sll $t3, $t3, 2 # multiply x coordinate by 4

    add $t9, $t9, $t4
    add $t9, $t9, $t3

    # Now $t9 stores the coordinate of the virus -> just store the value there
    jal get_virus_colour

    lw $t5, 0($sp) #$t5 stores the color
    addi $sp, $sp, 4

    sw $t5, 0($t9)

    # Increment the counter
    addi $a2, $a2, 1

    j viruses_loop
  
end_viruses_loop:
    lw $ra, 0($sp)   # Restore return address
    addi $sp, $sp, 4
    jr $ra

get_virus_colour:
    # Case 0 -> Red
    lw   $t7, VIRUS_RED       # Prepare red in $t7
    beq  $a0, $zero, end_virus_color  # if ($a0 == 0) => done
  
    # Case 1 -> Blue
    lw   $t7, VIRUS_BLUE       # Prepare blue in $t7
    li   $t6, 1
    beq  $a0, $t6, end_virus_color   # if ($a0 == 1) => done
  
    # Case 2 -> Yellow
    lw $t7, VIRUS_YELLOW

end_virus_color:
    # Push the resulting color on the stack
    addi $sp, $sp, -4
    sw $t7, 0($sp)
    jr $ra                  # Return (output stored in $t5)

get_leftmost_coordinate:
    la $t0, BOARD   # $t0 = base address for display
    lw $t1, SCENE_X     #$t1 - x coordinate of the leftmost point of the bottle
    lw $t2, SCENE_Y     #$t2 - y coordinate of the leftmost point of the bottle
    sll $t2, $t2, 8     # multiple y coordinate by 128
    sll $t1, $t1, 2     # multiple x coordinate by 4
    add $t0, $t0, $t1   
    add $t0, $t0, $t2   # change $t0 to correspond to the leftmost pixel of the bottle

    # Push the result on the stack

    addi $sp, $sp, -4
    sw $t0, 0($sp)

    jr $ra

draw_background:
  la $a0, BOARD
  lw $a1, image_height
  lw $a2, image_width
  la $a3, image_pixels

  li $t3, 0

  save_registers()

  jal row_loop_background

  restore_registers()

  save_registers()

  jal transfer_to_bitmap


  restore_registers()

  
  lw $a1, mario_height
  lw $a2, mario_width
  la $a3, mario_pixels

  li $t9, 35 # Offset for mario

  sll $t9, $t9, 2 # Multiply x offset by 4

  add $a0, $a0, $t9

  li $t9, 5

  sll $t9, $t9, 8 # Multiply 4 offset by 256

  add $a0, $a0, $t9

  save_registers()

  jal row_loop_mario

  restore_registers()
  
  jr $ra


row_loop_mario:
  # $a0 -> coordinate
  # $a1 -> height
  # $a2 -> width
  # $t3 -> counter
  # a3 -> start of the array
    beq  $t3, $a1, done      # If row counter equals height, finish
    li   $t4, 0              # Column counter (x = 0)

    save_registers()

    jal transfer_to_bitmap

    restore_registers()

col_loop_mario:
    beq  $t4, $a2, next_row_mario  # If column counter equals width, go to next row
    lw $t5, 0($a3)         # Load current pixel into $t5
    
    lw $t9, 0($a0)

    lw $t7, MARIO_BACKGROUND

    beq $t5, $t7, skip_pixel
    
    # Process the pixel in $t5 here
    sw $t5, 0($a0)

    addi $a3, $a3, 4

    addi $a0, $a0, 4         # Advance pointer to next pixel (each pixel is 4 bytes)
    addi $t4, $t4, 1         # Increment column counter
    j col_loop_mario

next_row_mario:
    addi $t3, $t3, 1         # Increment row counter
    # Reset the pixel to go back to the initial position + 1 down

    addi $a0, $a0, 256

    subi $a0, $a0, 88
  
    j row_loop_mario


skip_pixel:
  addi $a3, $a3, 4
  addi $a0, $a0, 4         # Advance pointer to next pixel (each pixel is 4 bytes)
  addi $t4, $t4, 1         # Increment column counter
  
  j col_loop_mario


row_loop_background:
  # $a0 -> coordinate
  # $a1 -> height
  # $a2 -> width
  # $t3 -> counter
  # a3 -> start of the array
    beq  $t3, $a1, done      # If row counter equals height, finish
    li   $t4, 0              # Column counter (x = 0)

    save_registers()

    jal transfer_to_bitmap

    restore_registers()

col_loop_background:
    beq  $t4, $a2, next_row_background  # If column counter equals width, go to next row
    lw $t5, 0($a3)         # Load current pixel into $t5
    
    lw $t9, 0($a0)

    lw $t8, GRAY

    beq $t9, $t8, wait_until_second_gray    
    # Process the pixel in $t5 here
    sw $t5, 0($a0)

    addi $a3, $a3, 4

    addi $a0, $a0, 4         # Advance pointer to next pixel (each pixel is 4 bytes)
    addi $t4, $t4, 1         # Increment column counter
    j col_loop_background

next_row_background:
    addi $t3, $t3, 1         # Increment row counter
  
    j row_loop_background

wait_until_second_gray:

  beq $t3, 39, skip_ground
  
  addi $a3, $a3, 4
  addi $a0, $a0, 4
  addi $t4, $t4, 1         # Increment column counter

  lw $t9, 0($a0)

  lw $t8, GRAY

  beq $t9, $t8, end_wait

  j wait_until_second_gray
  
end_wait:
  addi $a3, $a3, 4
  addi $a0, $a0, 4
  addi $t4, $t4, 1         # Increment column counter

  j col_loop_background

skip_ground:
  lw $t9, 0($a0)

  bne $t9, $t8, end_skip_ground

  addi $a3, $a3, 4
  addi $a0, $a0, 4
  addi $t4, $t4, 1         # Increment column counter

  j skip_ground


end_skip_ground:
  j col_loop_background

done:
  jr $ra

  
exit:
    li $v0, 10              # terminate the program gracefully
    syscall