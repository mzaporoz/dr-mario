shade_loop:
    beq $t7, 2560, done 
    lw    $t0, 0($a0)        # load current pixel (0x00RRGGBB)

    # Extract red channel (bits 16-23)
    srl   $t1, $t0, 16       # shift right 16: t1 = 0x000000RR
    andi  $t1, $t1, 0xFF     # mask to get red (0-255)

    # Extract green channel (bits 8-15)
    srl   $t2, $t0, 8        # shift right 8: t2 = 0x0000RRGG
    andi  $t2, $t2, 0xFF     # mask to get green

    # Extract blue channel (bits 0-7)
    andi  $t3, $t0, 0xFF     # t3 = blue

    # Multiply each channel by 3 then divide by 4 (approx. darkening)
    li    $t4, 3             # constant 3

    mul   $t1, $t1, $t4      # red = red * 3
    srl   $t1, $t1, 2        # red = (red*3) >> 2, i.e. divided by 4

    mul   $t2, $t2, $t4      # green = green * 3
    srl   $t2, $t2, 2        # green = (green*3) >> 2

    mul   $t3, $t3, $t4      # blue = blue * 3
    srl   $t3, $t3, 2        # blue = (blue*3) >> 2

    # Recombine channels into a single 32-bit word (0x00RRGGBB)
    sll   $t1, $t1, 16       # shift red left 16 bits
    sll   $t2, $t2, 8        # shift green left 8 bits
    or    $t0, $t1, $t2      # combine red and green
    or    $t0, $t0, $t3      # add blue

    sw    $t0, 0($a0)        # store the new pixel back

    addi  $a0, $a0, 4        # move to the next pixel (4 bytes per pixel)

    addi $t7, $t7, 1

    j shade_loop


