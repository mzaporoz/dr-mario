.macro save_registers()
    addi $sp, $sp, -60      # Reserve 60 bytes
    sw   $ra, 56($sp)
    sw   $t0, 52($sp)       # Save $t0
    sw   $t1, 48($sp)       # Save $t1
    sw   $t2, 44($sp)       # Save $t2
    sw   $t3, 40($sp)       # Save $t3
    sw   $t4, 36($sp)       # Save $t4
    sw   $t5, 32($sp)       # Save $t5
    sw   $t6, 28($sp)       # Save $t6
    sw   $t7, 24($sp)       # Save $t7
    sw   $t8, 20($sp)       # Save $t8
    sw   $t9, 16($sp)       # Save $t9
    sw   $a0, 12($sp)       # Save $a0
    sw   $a1, 8($sp)        # Save $a1
    sw   $a2, 4($sp)        # Save $a2
    sw   $a3, 0($sp)        # Save $a3
.end_macro

.macro restore_registers()
    lw   $a3, 0($sp)        # Restore $a3
    lw   $a2, 4($sp)        # Restore $a2
    lw   $a1, 8($sp)        # Restore $a1
    lw   $a0, 12($sp)       # Restore $a0
    lw   $t9, 16($sp)       # Restore $t9
    lw   $t8, 20($sp)       # Restore $t8
    lw   $t7, 24($sp)       # Restore $t7
    lw   $t6, 28($sp)       # Restore $t6
    lw   $t5, 32($sp)       # Restore $t5
    lw   $t4, 36($sp)       # Restore $t4
    lw   $t3, 40($sp)       # Restore $t3
    lw   $t2, 44($sp)       # Restore $t2
    lw   $t1, 48($sp)       # Restore $t1
    lw   $t0, 52($sp)       # Restore $t0
    lw   $ra, 56($sp)
    addi $sp, $sp, 60       # Deallocate stack space
.end_macro

.macro save_ra()
    addi $sp, $sp, -4
	sw $ra, 0($sp)
.end_macro

.macro restore_ra()
    lw $ra, 0($sp)   # Restore return address
	addi $sp, $sp, 4
.end_macro

