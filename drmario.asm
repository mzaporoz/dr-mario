row_paused_loop:
  # $a0 -> coordinate
  # $a1 -> height
  # $a2 -> width
  # $t3 -> counter
  # a3 -> start of the array
    beq  $t3, $a1, done      # If row counter equals height, finish
    li   $t4, 0              # Column counter (x = 0)

col_paused_loop:
    beq  $t4, $a2, next_paused_row  # If column counter equals width, go to next row
    lw $t5, 0($a3)         # Load current pixel into $t5
    # Process the pixel in $t5 here

    lw $t8, MARIO_BACKGROUND

    beq $t8, $t5, skip_pixel_paused
    
    sw $t5, 0($a0)

    addi $a3, $a3, 4

    addi $a0, $a0, 4         # Advance pointer to next pixel (each pixel is 4 bytes)
    addi $t4, $t4, 1         # Increment column counter
    j col_paused_loop

