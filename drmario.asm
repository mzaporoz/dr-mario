menu:
  # 0 -> level easy (3 viruses, 8 cycles for gravity)
  # 1 -> level medium (5 viruses, 4 cycles for gravity)
  # 2 -> level hard (7 viruses, 2 cycles for gravity)

  # Draw menu first
  jal draw_menu

  # Now, draw cursor

  li $t9, 7

  li $t8, 13

  lw $t0, ADDR_DSPL
  sll $t9, $t9, 2 # Multiply by 4 because x offset

  add $t0, $t0, $t9

  sll $t8, $t8, 8 # Multiply by 8 because y offset

  add $t0, $t0, $t8

  sw $t0, CUR_CURSOR_ADDRESS

  jal draw_cursor

  # Now, we toggle the cursor and wait for user input

  li $t0, 0

  sw, $t0, CUR_LEVEL

  j toggle_and_wait_for_level

  j exit

toggle_and_wait_for_level:
  # 1a. Check if key has been pressed
  lw $t0, ADDR_KBRD               # $t0 = base address for keyboard
  lw $t1, 0($t0)                  # Load first word from keyboard

  save_registers()
  # 1b. Check which key has been pressed
  beq $t1, 1, check_if_q_or_arrow_or_enter      # If first word 1, key is pressed

  restore_registers()

  
  # Here, we draw white screen to replace the toggle
  lw $a0, CUR_CURSOR_ADDRESS

  lw $a1, cursor_height
  lw $a2, cursor_width

  la $a3, cursor_toggle_pixels

  # t0 will be our row index (i). Initialize to 0.
  li $t3, 0 

  save_registers()
  
  jal row_paused_loop

  restore_registers()

  # Here, we sleep

  li $v0, 32
  li $a0, 400
  syscall

  # Redraw cursor

  save_registers()

  jal draw_cursor

  restore_registers()

  # Here, we sleep

  li $v0, 32
  li $a0, 400
  syscall

  # Loop back

  j toggle_and_wait_for_level


check_if_q_or_arrow_or_enter:
  lw $a0, 4($t0)                  # Load second word from keyboard

  beq $a0, 0x71, exit     # Check if the key q was pressed

  beq $a0, 0x0A, set_up_level

  beq $a0, 0x73, go_down

  beq $a0, 0x77, go_up


  j toggle_and_wait_for_level
    

