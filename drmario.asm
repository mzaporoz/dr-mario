game_loop:
  lw $t2, GRAVITY # Load how many cycles need for gravity
  
  # $a0 stores the counter for how many times we called sleep call -> if it equals to 63, then a second has passed and we drop a capsule by 1 row

  lw $t1, CUR_NUM_VIRUSES
  
  beq $t1, 0, trigger_game_over_win

  lw $t3, NUM_VIRUSES
  bge $s2, $t3, increase_speed  # once we've cleared NUM_VIRUSES rows, increase speed
  
  beq $s0, $t2, set_gravity_to_zero

