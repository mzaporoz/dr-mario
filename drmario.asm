    lw $t0, SET_INDEX

    sll $t0, $t0, 2
    la $t1, MUSIC
    add $t0, $t1, $t0

    lw $t1, 0($t0)            # grab set address
    beq $t1, -1, reset_music  # reached the end of the tune

    lw $t2, 0($t1)            # grab timestamp of set
    sra $t2, $t2, 2           # this basically determines the speed
    
